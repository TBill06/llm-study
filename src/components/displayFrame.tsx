type ResponseType = {
    type: 'text' | 'image';
    content: string;
};

type Props = {
    response: ResponseType | null;
};
  
  const Display = ({ response }: Props) => {
    return (
      <div className="max-w-3xl mx-auto my-4 p-4 border border-gray-300 rounded-md shadow-md">
        <h1 className="text-xl font-bold mb-2">Response</h1>
        {response ? (
            response.type === 'image' ? (
                <div className="flex justify-center items-center">
                    <img src={response.content} alt="Generated by DALL-E" className="w-3/4 h-auto"/>
                </div>
            ) : (
                <p>{response.content}</p>
            )
        ) : (
            <p>No response yet...</p>
        )}
      </div>
    );
  };
  
  export default Display;