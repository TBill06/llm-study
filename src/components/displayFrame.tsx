import { motion, AnimatePresence } from 'framer-motion';
import { fadeIn } from '../transitions';

type ResponseType = {
    type: 'text' | 'image';
    content: string;
};

type Props = {
    response: ResponseType | null;
    onContinue: () => void;
};
  
const Display = ({ response, onContinue }: Props) => {
    return (
        <motion.div 
            initial="hidden"
            animate="visible"
            variants={fadeIn}
            className="max-w-3xl mx-auto my-4 p-6 bg-white rounded-lg shadow-lg"
        >
            <h1 className="text-2xl font-bold mb-4 text-gray-800">Response</h1>
            <AnimatePresence mode="wait">
                {response ? (
                    <motion.div
                        key={response.content}
                        initial={{ opacity: 0, y: 20 }}
                        animate={{ opacity: 1, y: 0 }}
                        exit={{ opacity: 0, y: -20 }}
                        transition={{ duration: 0.5 }}
                    >
                        {response.type === 'image' ? (
                            <div className="relative group">
                                <motion.img 
                                    src={response.content} 
                                    alt="Generated by DALL-E" 
                                    className="w-3/4 h-auto mx-auto rounded-lg shadow-md transform transition-transform duration-300 group-hover:scale-102"
                                    layoutId="response-image"
                                />
                            </div>
                        ) : (
                            <motion.p 
                                className="text-lg text-gray-700 leading-relaxed"
                                initial={{ opacity: 0 }}
                                animate={{ opacity: 1 }}
                                transition={{ duration: 0.5, delay: 0.2 }}
                            >
                                {response.content}
                            </motion.p>
                        )}

                        <motion.button
                            onClick={onContinue}
                            whileHover={{ scale: 1.02 }}
                            whileTap={{ scale: 0.98 }}
                            className="mt-4 px-6 py-3 bg-gradient-to-r from-blue-600 to-purple-600 
                                     text-white rounded-lg shadow-md hover:shadow-lg transition-all duration-300"
                        >
                            Continue to Survey
                        </motion.button>
                    </motion.div>
                ) : (
                    <motion.p 
                        className="text-gray-500 italic"
                        initial={{ opacity: 0 }}
                        animate={{ opacity: 1 }}
                        exit={{ opacity: 0 }}
                    >
                        Waiting for your creative prompt...
                    </motion.p>
                )}
            </AnimatePresence>
        </motion.div>
    );
};
  
export default Display;